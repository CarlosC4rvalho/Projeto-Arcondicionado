package Project;

public class Arconditioning {

	        //attributes
		private String Brand, Model, Btus;
		private int Temperature = 23;
		private String ModeOp = "Refrigeration";
		
		//get and set
		public String getBrand() {
			return Brand;
		}
		public void setBrand(String brand) {
			Brand = brand;
		}
		public String getModel() {
			return Model;
		}
		public void setModel(String model) {
			Model = model;
		}
		public String getBtus() {
			return Btus;
		}
		public void setBtus(String btus) {
			Btus = btus;
		}
		public int getTemperature() {
			return Temperature;
		}
		public void setTemperature(int temperature) {
			Temperature = temperature;
		}
		public String getModeOp() {
			return ModeOp;
		}
		public void setModeOp(String modeOp) {
			ModeOp = modeOp;
		}
		
}
		
